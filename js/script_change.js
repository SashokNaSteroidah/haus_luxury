function processSelectedFiles(fileInput) {
    var files = fileInput.files;

    for (var i = 0; i < files.length; i++) {
        document.querySelector('.output1').innerHTML = "Filename " + files[i].name
    }
    document.querySelector('.delete_img').style.display = "block"
    document.querySelector('.file1').style.fill = 'green'
}
document.querySelector('.delete_img').onclick = () =>{
    document.querySelector('#input__file1').value = ''
    document.querySelector('.delete_img').style.display = "none"
    document.querySelector('.output1').innerHTML = ''
    document.querySelector('.file1').style.fill = 'black'
}

